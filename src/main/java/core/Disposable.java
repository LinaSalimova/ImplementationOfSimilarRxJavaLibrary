package core;

/**
 * Интерфейс Disposable — контракт для управления жизненным циклом подписки.
 * Позволяет отменить подписку (например, чтобы прекратить получение событий от Observable)
 * и проверить, отменена ли она.
 */
public interface Disposable {
    /**
     * Отменяет подписку/ресурс.
     * После вызова dispose() Observable должен прекратить отправку событий этому подписчику.
     */
    void dispose();

    /**
     * Проверяет, отменена ли подписка/ресурс.
     * @return true, если подписка отменена, иначе false
     */
    boolean isDisposed();
}
